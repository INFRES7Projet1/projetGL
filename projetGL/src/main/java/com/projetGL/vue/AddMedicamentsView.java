    private void jButtonAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAjouterActionPerformed
        medocDao = new MedicamentDAO();
        pmedoc = new Medicament();
        SimpleDateFormat formater = new SimpleDateFormat("yyyy-mm-dd");
        
        pmedoc.Id = parseInt(jTextFieldID.getText());
        pmedoc.Produit = jTextFieldProduit.getText();
        pmedoc.Quantite = parseInt(jTextFieldQuantite.getText());
        pmedoc.FormeDosage = jTextFieldFormeDosage.getText();
        pmedoc.Lot = jTextFieldLot.getText();
        pmedoc.Dotation = jTextFieldDotation.getText();
        DCI dci = new DCI((Integer)jComboBoxDCI.getSelectedItem());
        pmedoc.Dci= dci;
        
        try {
            pmedoc.Dlu =  (java.util.Date)formater.parse(jTextFieldDLU.getText());
        } 
        catch (ParseException ex) {
            Logger.getLogger(AddMedicamentsView.class.getName()).log(Level.SEVERE, null, ex);
        }
        medocDao.create(pmedoc);
        
        colisdao.InsertMedicamentInColis(pmedoc.Id, parseInt(jComboBoxColis.getSelectedItem().toString()));
        
        this.dispose();
    }//GEN-LAST:event_jButtonAjouterActionPerformed

