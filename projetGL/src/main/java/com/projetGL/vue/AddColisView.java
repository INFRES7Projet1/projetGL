    private void jButtonAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAjouterActionPerformed
        colisDao = new ColisDAO();
        pcolis = new Colis();
        
        pcolis.Id = parseInt(jTextFieldID.getText());
        pcolis.Designation = jTextFieldDesignation.getText();
        pcolis.Affectataire = jTextFieldAffectataire.getText();
        pcolis.Poids = parseInt(jTextFieldPoids.getText());
        pcolis.Etat = (String) jComboBoxEtat.getSelectedItem();
        TypeColis tc = new TypeColis( (Integer)jComboBoxType.getSelectedItem());
        pcolis.Type = tc;

        OptionColis op = new OptionColis( (Integer)jComboBoxOption.getSelectedItem());
        pcolis.Option = op;
        
        Secteur sec = new Secteur( (Integer)jComboBoxSecteur.getSelectedItem());
        pcolis.SecteurUtilisation = sec;
        colisDao.create(pcolis);
        
        confcolisdao.InsertColisInConfiguration(pcolis.Id, parseInt(jComboBoxConf.getSelectedItem().toString()));
        this.dispose();
    }//GEN-LAST:event_jButtonAjouterActionPerformed

