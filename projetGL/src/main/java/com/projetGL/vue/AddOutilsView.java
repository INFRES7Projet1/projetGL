    private void jButtonAjouterActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAjouterActionPerformed
        outildao = new OutilDAO();
        outil = new Outil();
        SimpleDateFormat formater = new SimpleDateFormat("yyyy-mm-dd");
        
        outil.Designation = jTextFieldDesignation.getText();
        outil.Quantite = parseInt(jTextFieldQuantite.getText());
        outil.Reference = jTextFieldReference.getText();
        outil.Id = parseInt(jTextFieldID.getText());
        try {
            outil.Dlu =  formater.parse(jTextFieldDLU.getText());
        } 
        catch (ParseException ex) {
            Logger.getLogger(AddOutilsView.class.getName()).log(Level.SEVERE, null, ex);
        }
        outildao.create(outil);
        
        colisdao.InsertOutilInColis(outil.Id, parseInt(jComboBoxColis.getSelectedItem().toString()));
        this.dispose();
    }//GEN-LAST:event_jButtonAjouterActionPerformed

